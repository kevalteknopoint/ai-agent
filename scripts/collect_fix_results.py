#!/usr/bin/env python3
"""collect_fix_results.py <repoPath>

Collects all per-batch fix results from analysis/.fix/*-results.json,
merges them into a single fix-results.json, and generates the human-readable
fix-report.md and fix-log.md.

Run after all fixer agents have completed their batches.

Output:
  - analysis/fix-results.json  (machine-parseable, all batches merged)
  - analysis/fix-report.md     (human-readable summary)
  - analysis/fix-log.md        (chronological change log)
"""
import datetime
import json
import os
import sys
from collections import defaultdict


def load_batch_results(repo_path):
    """Load all batch result files from analysis/.fix/"""
    fix_dir = os.path.join(repo_path, "analysis", ".fix")
    if not os.path.isdir(fix_dir):
        print(f"ERROR: No .fix/ folder at {fix_dir}", file=sys.stderr)
        sys.exit(1)

    all_results = []
    for fname in sorted(os.listdir(fix_dir)):
        if fname.endswith("-results.json"):
            fpath = os.path.join(fix_dir, fname)
            try:
                with open(fpath, "r", encoding="utf-8") as f:
                    data = json.load(f)
                all_results.append(data)
            except (json.JSONDecodeError, OSError) as e:
                print(f"WARNING: Skipping {fname}: {e}", file=sys.stderr)

    return all_results


def merge_results(batch_results):
    """Merge all batch results into a single structure."""
    merged = {
        "generatedAt": datetime.datetime.now().isoformat(),
        "totalFixed": 0,
        "totalFailed": 0,
        "totalSkipped": 0,
        "batches": [],
        "issues": [],
    }

    for batch in batch_results:
        batch_id = batch.get("batchId", "unknown")
        results = batch.get("results", [])

        fixed = len([r for r in results if r.get("status") == "Fixed"])
        failed = len([r for r in results if r.get("status") == "FixFailed"])
        skipped = len([r for r in results if r.get("status") == "Skipped"])

        merged["totalFixed"] += fixed
        merged["totalFailed"] += failed
        merged["totalSkipped"] += skipped
        merged["batches"].append({
            "batchId": batch_id,
            "fixed": fixed,
            "failed": failed,
            "skipped": skipped,
        })
        merged["issues"].extend(results)

    return merged


def generate_report(merged, repo_path):
    """Generate human-readable fix-report.md"""
    total = merged["totalFixed"] + merged["totalFailed"] + merged["totalSkipped"]
    lines = [
        "# Code Fix Report",
        "",
        f"**Generated:** {merged['generatedAt']}",
        f"**Repository:** `{os.path.basename(repo_path)}`",
        "",
        "## Summary",
        "",
        f"| Metric | Count |",
        f"|--------|-------|",
        f"| Total Issues Processed | {total} |",
        f"| Fixed | {merged['totalFixed']} |",
        f"| Fix Failed | {merged['totalFailed']} |",
        f"| Skipped | {merged['totalSkipped']} |",
        f"| Success Rate | {merged['totalFixed'] / max(total, 1) * 100:.1f}% |",
        "",
        "## Batch Results",
        "",
        "| Batch | Fixed | Failed | Skipped |",
        "|-------|-------|--------|---------|",
    ]

    for batch in merged["batches"]:
        lines.append(
            f"| {batch['batchId']} | {batch['fixed']} | {batch['failed']} | {batch['skipped']} |"
        )

    # Group by status for detail sections
    fixed_issues = [i for i in merged["issues"] if i.get("status") == "Fixed"]
    failed_issues = [i for i in merged["issues"] if i.get("status") == "FixFailed"]

    if fixed_issues:
        lines.extend(["", "## Fixed Issues", ""])
        for issue in fixed_issues:
            lines.append(f"- **{issue.get('id', '?')}** — {issue.get('fixSummary', 'N/A')}")
            lines.append(f"  - File: `{issue.get('file', '?')}`")

    if failed_issues:
        lines.extend(["", "## Failed Fixes (Require Manual Intervention)", ""])
        for issue in failed_issues:
            lines.append(f"- **{issue.get('id', '?')}** — {issue.get('failReason', 'Unknown')}")
            lines.append(f"  - File: `{issue.get('file', '?')}`")

    lines.extend(["", "---", "", "*Generated by bmad-code-fix*"])
    return "\n".join(lines)


def generate_log(merged):
    """Generate chronological fix-log.md"""
    lines = [
        "# Fix Change Log",
        "",
        f"**Generated:** {merged['generatedAt']}",
        "",
        "## Changes Applied",
        "",
    ]

    for issue in merged["issues"]:
        if issue.get("status") == "Fixed":
            lines.append(f"### {issue.get('id', '?')} — {issue.get('fixSummary', '')}")
            lines.append(f"- **File:** `{issue.get('file', '?')}`")
            lines.append(f"- **Lines:** {issue.get('linesChanged', [])}")
            lines.append("")

    return "\n".join(lines)


def main():
    if len(sys.argv) < 2:
        print("Usage: collect_fix_results.py <repoPath>", file=sys.stderr)
        sys.exit(1)

    repo_path = sys.argv[1]
    batch_results = load_batch_results(repo_path)

    if not batch_results:
        print("No batch results found in analysis/.fix/")
        sys.exit(0)

    merged = merge_results(batch_results)

    # Write outputs
    analysis_dir = os.path.join(repo_path, "analysis")

    with open(os.path.join(analysis_dir, "fix-results.json"), "w", encoding="utf-8") as f:
        json.dump(merged, f, indent=2, ensure_ascii=False)

    with open(os.path.join(analysis_dir, "fix-report.md"), "w", encoding="utf-8") as f:
        f.write(generate_report(merged, repo_path))

    with open(os.path.join(analysis_dir, "fix-log.md"), "w", encoding="utf-8") as f:
        f.write(generate_log(merged))

    print(json.dumps({
        "totalFixed": merged["totalFixed"],
        "totalFailed": merged["totalFailed"],
        "totalSkipped": merged["totalSkipped"],
        "reportPath": os.path.join(analysis_dir, "fix-report.md"),
    }, indent=2))


if __name__ == "__main__":
    main()
